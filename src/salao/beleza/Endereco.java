package salao.beleza;

public class Endereco {

    private String logradouro;
    private int numero;
    private String bairro;
    private String cidade;
    private String uf;
    private String rua;


    public Endereco(String logradouro, int numero, String bairro, String cidade,String rua, String uf){
        this.logradouro = logradouro;
        this.numero = numero;
        this.bairro = bairro;
        this.cidade = cidade;
        this.uf = uf;
        this.rua = rua;

    }




}
